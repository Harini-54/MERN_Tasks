let weather="Cloudy";
switch (weather){
    case "Sunny":
        console.log("It's sunny outside! Wear sunglasses and stay hydrated");
        break;
    case "Cloudy":
        console.log("It's a bit gloomy. A light jacket might be good idea.");
        break;
    case "Rainy":
        console.log("It's raining. Bring an umbrella and wear a raincoat!");
        break;
    case "Snowy":
        console.log("It's cold and snowy! Wear warm clothes, gloves and a hat");
        break;
    case "Windy":
        console.log("It's windy!! Be careful");
        break;
    default:
        console.log("I don't know what the weather is like.");
}

/*
output:
It's a bit gloomy. A light jacket might be good idea.
*/
